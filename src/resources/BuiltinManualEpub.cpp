bool ensureInstalled() {
  Storage.mkdir("/book");

  {
    HalFile existing;
    if (Storage.openFileForRead("MAN", kManualPath, existing)) {
      const bool sameSize = existing.fileSize() == kBuiltinManualEpubSize;
      existing.close();
      if (sameSize) return true;
      LOG_INF("MAN", "Replacing built-in manual: size changed");
    }
  }

  HalFile file;
  if (!Storage.openFileForWrite("MAN", kManualPath, file)) {
    LOG_ERR("MAN", "Failed to create built-in manual: %s", kManualPath);
    return false;
  }

  size_t written = 0;
  while (written < kBuiltinManualEpubSize) {
    const size_t chunk = std::min<size_t>(256, kBuiltinManualEpubSize - written);
    const size_t n = file.write(kBuiltinManualEpubData + written, chunk);
    if (n == 0) break;
    written += n;
  }
  file.close();

  if (written != kBuiltinManualEpubSize) {
    LOG_ERR("MAN", "Failed to write built-in manual: %u/%u bytes", static_cast<unsigned>(written),
            static_cast<unsigned>(kBuiltinManualEpubSize));
    return false;
  }

  LOG_INF("MAN", "Installed built-in manual: %s (%u bytes)", kManualPath,
          static_cast<unsigned>(kBuiltinManualEpubSize));
  return true;
}

