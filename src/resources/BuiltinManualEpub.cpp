bool ensureInstalled() {
  if (!Storage.ensureDirectoryExists("/book")) {
    LOG_ERR("MANUAL", "Failed to ensure /book directory");
    return false;
  }

  if (Storage.exists(kManualPath)) {
    FsFile existing;
    if (Storage.openFileForRead("MANUAL", kManualPath, existing)) {
      const bool sameSize = existing.fileSize() == kBuiltinManualEpubSize;
      existing.close();
      if (sameSize) return true;
    }
    Storage.remove(kManualPath);
  }

  FsFile file;
  if (!Storage.openFileForWrite("MANUAL", kManualPath, file)) {
    LOG_ERR("MANUAL", "Failed to create built-in manual EPUB");
    return false;
  }

  size_t written = 0;
  while (written < kBuiltinManualEpubSize) {
    const size_t chunk = std::min<size_t>(256, kBuiltinManualEpubSize - written);
    const size_t n = file.write(kBuiltinManualEpubData + written, chunk);
    if (n == 0) break;
    written += n;
  }
  file.close();

  if (written != kBuiltinManualEpubSize) {
    LOG_ERR("MANUAL", "Manual write incomplete: %u/%u", static_cast<unsigned>(written),
            static_cast<unsigned>(kBuiltinManualEpubSize));
    Storage.remove(kManualPath);
    return false;
  }

  LOG_DBG("MANUAL", "Installed built-in manual: %s (%u bytes)", kManualPath,
          static_cast<unsigned>(kBuiltinManualEpubSize));
  return true;
}

