// measurement of power button press duration calibration value
unsigned long t1 = 0;
unsigned long t2 = 0;

// Verify power button press duration on wake-up from deep sleep
// Pre-condition: isWakeupByPowerButton() == true
void verifyPowerButtonDuration() {
  if (SETTINGS.shortPwrBtn == CrossPointSettings::SHORT_PWRBTN::SLEEP) {
    // Fast path for short press
    // Needed because inputManager.isPressed() may take up to ~500ms to return the correct state
    return;
  }

  // Give the user up to 1000ms to start holding the power button, and must hold for SETTINGS.getPowerButtonDuration()
  const auto start = millis();
  bool abort = false;
  // Subtract the current time, because inputManager only starts counting the HeldTime from the first update()
  // This way, we remove the time we already took to reach here from the duration,
  // assuming the button was held until now from millis()==0 (i.e. device start time).
  const uint16_t calibration = start;
  const uint16_t calibratedPressDuration =
      (calibration < SETTINGS.getPowerButtonDuration()) ? SETTINGS.getPowerButtonDuration() - calibration : 1;

  gpio.update();
  // Needed because inputManager.isPressed() may take up to ~500ms to return the correct state
  while (!gpio.isPressed(HalGPIO::BTN_POWER) && millis() - start < 1000) {
    delay(10);  // only wait 10ms each iteration to not delay too much in case of short configured duration.
    gpio.update();
  }

  t2 = millis();
  if (gpio.isPressed(HalGPIO::BTN_POWER)) {
    do {
      delay(10);
      gpio.update();
    } while (gpio.isPressed(HalGPIO::BTN_POWER) && gpio.getHeldTime() < calibratedPressDuration);
    abort = gpio.getHeldTime() < calibratedPressDuration;
  } else {
    abort = true;
  }

  if (abort) {
    // Button released too early. Returning to sleep.
    // IMPORTANT: Re-arm the wakeup trigger before sleeping again
    powerManager.startDeepSleep(gpio);
  }
}

void waitForPowerRelease() {
  gpio.update();
  while (gpio.isPressed(HalGPIO::BTN_POWER)) {
    delay(50);
    gpio.update();
  }
}

// Enter deep sleep mode
void enterDeepSleep() {
  HalPowerManager::Lock powerLock;  // Ensure we are at normal CPU frequency for sleep preparation
  APP_STATE.lastSleepFromReader = activityManager.isReaderActivity();
  APP_STATE.saveToFile();

  activityManager.goToSleep();

  // 等待睡眠圖片的最後一次 EPD 更新完全穩定。
  delay(100);  

  display.deepSleep();
  LOG_DBG("MAIN", "Power button press calibration value: %lu ms", t2 - t1);
  LOG_DBG("MAIN", "Entering deep sleep");

  powerManager.startDeepSleep(gpio);
}
void setupExternalFonts() {
  // 確保 SD 卡根目錄有 /fonts。
  // 資料夾已存在時可忽略 mkdir 的回傳值。
  Storage.mkdir("/fonts");

  FontManager& fontManager = FontManager::getInstance();

  // 必須先掃描，loadSettings() 才能用保存的檔名
  // 對應到這次掃描出的字型 index。
  fontManager.scanFonts();

  const int fontCount = fontManager.getFontCount();

  LOG_INF(
      "MAIN",
      "External font scan complete: %d font(s)",
      fontCount
  );

  if (fontCount <= 0) {
    LOG_INF(
        "MAIN",
        "No external fonts found in /fonts; using built-in fonts"
    );
    return;
  }

  // 嘗試恢復 /.crosspoint/font_settings.bin 中的選擇。
  fontManager.loadSettings();

  // 暫時自動指定 Noto 14x20 為 UI 字型。
  constexpr const char* UI_FONT_FILENAME =
      "KingHwaOldSong_17_23x30.bin";
      // "NotoSansCJKTC_14_18x26.bin";

  bool uiFontFound = false;

  for (int i = 0; i < fontManager.getFontCount(); ++i) {
    const FontInfo* info = fontManager.getFontInfo(i);

    if (info == nullptr) {
      continue;
    }

    if (strcmp(info->filename, UI_FONT_FILENAME) != 0) {
      continue;
    }

    uiFontFound = true;

    if (fontManager.previewUiFont(i)) {
      fontManager.saveSettings();

      LOG_INF(
          "MAIN",
          "External UI font active: %s "
          "(%dpt, %dx%d)",
          info->filename,
          info->size,
          info->width,
          info->height
      );
    } else {
      LOG_ERR(
          "MAIN",
          "Failed to load external UI font: %s",
          info->filename
      );
    }

    break;
  }

  if (!uiFontFound) {
    LOG_ERR(
        "MAIN",
        "UI font not found: %s",
        UI_FONT_FILENAME
    );
  }

  // 第一階段尚未加入字型選單。
  // 若沒有保存的字型，或保存的字型已不存在，
  // 暫時自動載入掃描清單中的第 1 套字型。
  if (!fontManager.isExternalFontEnabled()) {
    LOG_INF(
        "MAIN",
        "No active external reader font; loading font index 0"
    );

    if (fontManager.previewFont(0)) {
      // previewFont() 不會自行保存，所以成功後手動保存。
      fontManager.saveSettings();
    } else {
      LOG_ERR(
          "MAIN",
          "Failed to load external font index 0"
      );
      return;
    }
  }

  const int selectedIndex = fontManager.getSelectedIndex();
  const FontInfo* selectedFont =
      fontManager.getFontInfo(selectedIndex);

  if (selectedFont != nullptr &&
      fontManager.isExternalFontEnabled()) {

    LOG_INF(
        "MAIN",
        "External reader font active: %s "
        "(file=%s, size=%u, cell=%ux%u)",
        selectedFont->name,
        selectedFont->filename,
        selectedFont->size,
        selectedFont->width,
        selectedFont->height
    );
  } else {
    LOG_ERR(
        "MAIN",
        "External font selection exists but font is not active"
    );
  }
}

